package com.iet.ietiansdiary;

public class uploadSyllabi {
    public String sname;
    public String surl;




    public uploadSyllabi() {
    }

    public uploadSyllabi(String sname, String surl) {
        this.sname = sname;
        this.surl = surl;
    }

    public String getSname() {
        return sname;
    }



    public String getSurl() {
        return surl;
    }


}
